class Instance:

    def __init__(self):
        self.dtos = []
        self.ars = []
        self.constants = {}
        self.paws = []
        self.dlos = []

    def get_dtos(self):
        return self.dtos

    def get_ars(self):
        return self.ars

    def get_constants(self):
        return self.constants

    def get_paws(self):
        return self.paws

    def get_dlos(self):
        return self.dlos
